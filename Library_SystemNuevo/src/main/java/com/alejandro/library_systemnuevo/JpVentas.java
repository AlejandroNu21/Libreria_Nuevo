    public void clearTable() {
        tblVenta.selectAll();
        int filas[] = tblVenta.getSelectedRows();
        int indice = filas.length - 1;
        for (int i = 0; i < filas.length; i++) {
            model.removeRow(indice);
            indice--;
        }
    }


